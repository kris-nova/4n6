package libxpid

// #cgo LDFLAGS: -lxpid
//
// #include "xpid.h"
// #include "stdlib.h"
import "C"
import "unsafe"

func BPFMapType(mapType int) string {
	var name string = ""
	cname := C.CString(name)
	C.bpf_map_type_enum(C.int(mapType), cname)
	defer C.free(unsafe.Pointer(cname))
	return C.GoString(cname)
}
