// #include "xpid.h"
import "C"

func ProcDirOpendir(pid int64) int {
	x := C.proc_dir_opendir(C.int(int(pid)))
	return int(x)
}

func ProcDirChdir(pid int64) int {
	x := C.proc_dir_chdir(C.int(int(pid)))
	return int(x)
}

func ProcDirDent(pid int64) int {
	x := C.proc_dir_dent(C.int(int(pid)))
	return int(x)
}
